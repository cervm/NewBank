package newbank.server;

/**
 * The type Customer id.
 */
public class CustomerID {
    private final String key;

    /**
     * Instantiates a new Customer id.
     *
     * @param key the key
     */
    public CustomerID(String key) {
        this.key = key;
    }

    /**
     * Gets key.
     *
     * @return the key
     */
    public String getKey() {
        return key;
    }
}
